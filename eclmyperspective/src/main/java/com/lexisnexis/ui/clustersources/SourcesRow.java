package com.lexisnexis.ui.clustersources;

public class SourcesRow {
	
	private String source;
	
	private String cooccurPcnt;
	
	private String cooccur;
	
	private String expectedcooccur;

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getCooccurPcnt() {
		return cooccurPcnt;
	}

	public void setCooccurPcnt(String cooccurPcnt) {
		this.cooccurPcnt = cooccurPcnt;
	}

	public String getCooccur() {
		return cooccur;
	}

	public void setCooccur(String cooccur) {
		this.cooccur = cooccur;
	}

	public String getExpectedcooccur() {
		return expectedcooccur;
	}

	public void setExpectedcooccur(String expectedcooccur) {
		this.expectedcooccur = expectedcooccur;
	}
}
