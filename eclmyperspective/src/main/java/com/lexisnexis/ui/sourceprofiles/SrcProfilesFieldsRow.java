package com.lexisnexis.ui.sourceprofiles;

public class SrcProfilesFieldsRow {

	private String field;
	
	private String uniqueValue;
	
	private String onlyValue;
	
	private String globallyUniqueValue;
	
	private String idsWithSrc;

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public String getUniqueValue() {
		return uniqueValue;
	}

	public void setUniqueValue(String uniqueValue) {
		this.uniqueValue = uniqueValue;
	}

	public String getOnlyValue() {
		return onlyValue;
	}

	public void setOnlyValue(String onlyValue) {
		this.onlyValue = onlyValue;
	}

	public String getGloballyUniqueValue() {
		return globallyUniqueValue;
	}

	public void setGloballyUniqueValue(String globallyUniqueValue) {
		this.globallyUniqueValue = globallyUniqueValue;
	}

	public String getIdsWithSrc() {
		return idsWithSrc;
	}

	public void setIdsWithSrc(String idsWithSrc) {
		this.idsWithSrc = idsWithSrc;
	}
}
